package com;

public class MathUtils {

    public int add(int a,int b){
        return a+b;
    }
    public int substract(int a,int b){
        return a-b;
    }

    public int divide(int a ,int b){
        return a/b;
    }

    public double computeCircleArea(double radius){
        return 3.14 * radius * radius;
    }


}
